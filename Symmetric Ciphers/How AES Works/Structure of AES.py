def bytes2matrix(text):
    """ Converts a 16-byte array into a 4x4 matrix.  """
    return [list(text[i:i+4]) for i in range(0, len(text), 4)]

#for every element in the matrix we convert it in its ascii character
def matrix2bytes(matrix):
    t=""
    for i in matrix:
        for j in i:
            t+=chr(j)
    return t
        

matrix = [
    [99, 114, 121, 112],
    [116, 111, 123, 105],
    [110, 109, 97, 116],
    [114, 105, 120, 125],
]

print(matrix2bytes(matrix))
